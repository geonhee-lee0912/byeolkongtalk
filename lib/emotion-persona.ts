/**
 * 감정 분류에 맞는 별콩이 톤 블록을 dynamicPart 주입용 문자열로 만든다.
 * 감정이 없거나 알 수 없는 값이면 빈 문자열 반환 (페르소나 기본 톤 유지).
 */
export function buildEmotionPersonaBlock(
  tag: EmotionTag | string | null | undefined
): string {
  if (!tag || !(tag in EMOTION_PERSONA)) return "";
  const block = EMOTION_PERSONA[tag as EmotionTag];
  return `\n\n## 이번 고민의 결 (감정 분류: ${tag})\n\n${block}\n\n> 이 결은 힌트일 뿐이야. 고민 내용이 실제로 다른 쪽을 향하면, 별콩이는 자연스럽게 그쪽 톤으로 옮겨가면 돼.`;
}
